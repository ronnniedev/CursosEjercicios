"""
Este es un modulo que realiza una suma
"""


def sumar(numero1, numero2):
    """
    Esta funcion acepta dos numeros para realizar la suma pertinente
    @:param numero1 : int
    @:param numero 2 : int
    @:return int
    """
    return numero1+numero2


SUMA = sumar(5, 7)

print(f"Resultado: {SUMA}")
